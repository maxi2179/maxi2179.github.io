/*
I- two numbers
O- return the smaller of the two numbers
if num is < num2 return num else
if num === num2 return num
*/
function min(num, num2) {
    if (num < num2) {
      return num;
    } else if (num2 < num) {
        return num2;
    } else if (num === num2) {
      return num;
    } else {
      return null;
    }
}
      

