// for everyremainder of 3 log 'fizz'and every remainder of 5 log 'buzz
// for remaider of both 3 and 5 log 'fizzbuzz'



function fizzBuzz(num, num2) {
  let result = [];
  for (let i = num; i <= num2; i++) {
    if (i % 5 === 0 && i % 3 === 0) {
      result.push('fizzbuzz');
    } else if (i % 3 === 0) {
      result.push('fizz');
    } else if (i % 5 === 0) {
      result.push('buzz');
    } else {
      result.push(i);
    }
    console.log(result);
  }
  
}




