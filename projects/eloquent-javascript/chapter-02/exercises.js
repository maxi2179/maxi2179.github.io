// make a loop that counts to 15
// for everyremainder of 3 log 'fizz'and every remainder of 5 log 'buzz
// for remaider of both 3 and 5 log 'fizzbuzz'

function fizzBuzz() {
  let count = 15;
  for (let i = 0; i <= count; i++) {
    if (i % 5 === 0 && i % 3 === 0) {
      console.log('fizzbuzz');
    } else if (i % 3 === 0) {
      console.log('fizz');
    } else if (i % 5 === 0) {
      console.log('buzz');
    } else {
      console.log(i);
    }
  };
  
}

